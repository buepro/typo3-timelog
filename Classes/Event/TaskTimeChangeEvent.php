namespace Buepro\Timelog\Event;

use Buepro\Timelog\Domain\Model\Task;

final class TaskTimeChangeEvent
{
    /** @var Task */
    protected $task;

    public function __construct(Task $task)
    {
        $this->task = $task;
    }

    public function getTask(): Task
    {
        return $this->task;
    }
}
